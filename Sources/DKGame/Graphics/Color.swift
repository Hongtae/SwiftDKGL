public class Color {
    public var red : Float = 0.0
    public var green : Float = 0.0
    public var blue : Float = 0.0
    public var alpha : Float = 1.0

    public init(red: Float, green: Float, blue: Float, alpha: Float) {
        self.red = red
        self.green = green
        self.blue = blue
        self.alpha = alpha
    }
}
